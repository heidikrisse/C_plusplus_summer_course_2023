// main.cpp
#include "include/basic_math_functions.h"
#include <iostream>

int main()
{
    std::cout << "4 + 2 = " << add(4, 2) << '\n';
    std::cout << "4 - 2 = " << subtract(4, 2) << '\n';
    std::cout << "4 * 2 = " << multiply(4, 2) << '\n';
    std::cout << "4 / 2 = " << divide(4, 2) << '\n';
    return 0;
}
